number = 2

# Left Shift (means divide by 2 to the power 1)
print(f"After Left Shift {number <<1}")

# Right Shift (means multiply by 2 to the power 1)
print(f"After Right Shift {number >>1}")

# XOR
print(f"After XOR with 1 {number ^ 1}")

# AND
print(f"After AND with 1 {number & 1}")

# OR
print(f"After OR with 1 {number | 1}")
